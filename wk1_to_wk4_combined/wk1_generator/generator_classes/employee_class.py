class Employee:
    def __init__(self, emp_id, emp_first_name, emp_last_name, emp_age, birth_date, join_date,
                 emp_salary, emp_manager_name, dept_name):
        self.emp_id = emp_id
        self.emp_first_name = emp_first_name
        self.emp_last_name = emp_last_name
        self.emp_age = emp_age
        self.birth_date = birth_date
        self.join_date = join_date
        self.emp_salary = emp_salary
        self.emp_manager_name = emp_manager_name
        self.dept_name = dept_name

